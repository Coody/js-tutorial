console.log("01- JavaScript Arithmetic Operators");
var x = 100 + 50;
console.log("x = " + x);

// Modulus
var x = 5;
var y = 2;
var z = x % y;
console.log("z = x % y = " + z);

// Incrementing
var x = 5;
x++;
var z = x;
console.log("z = " + z);


